	public String viewLoanBalanceReport(UtilityFunctions uF){
		
		Connection con = null;
		PreparedStatement pst = null;
		ResultSet rs = null;
		Database db = new Database();
		db.setRequest(request);
		
		try {
			
			con = db.makeConnection(con);
			
			Map<String, String> hmEmpName = CF.getEmpNameMap(con, null, null);
			List<List<String>> alReport = new ArrayList<List<String>>();
			List<String> alInner = new ArrayList<String>();
			
			Map<String, String> hmEmpCode = CF.getEmpCodeMap(con);
			if(hmEmpCode == null) hmEmpCode = new HashMap<String, String>();
			
			Map<String, String> hmEmpPanNo =CF.getEmpPANNoMap(con);	//added by parvez
			
			Map<String, String> hmEmpOrgId = CF.getEmpOrgIdList(con, uF);
			if(hmEmpOrgId == null) hmEmpOrgId = new HashMap<String, String>();
			Map<String, String> hmOrg = CF.getOrgName(con);
			if(hmOrg == null) hmOrg = new HashMap<String, String>();
				
			Map<String, String> hmAmountPaid = new HashMap<String, String>();
			pst = con.prepareStatement("select sum(amount_paid) as amt_paid, loan_id, emp_id from loan_payments where loan_id in (select loan_id from loan_applied_details where is_approved=1) group by loan_id, emp_id");
			rs = pst.executeQuery();
			while(rs.next()){
				hmAmountPaid.put(rs.getString("emp_id")+"_"+rs.getString("loan_id"), uF.getRoundOffValue(uF.parseToInt(CF.getRoundOffCondtion()),uF.parseToDouble(rs.getString("amt_paid"))));
			}
			rs.close();
			pst.close();
			
			if(getF_type()==null){
				setF_type("2");
			}
			
			StringBuilder sbQuery = new StringBuilder();
			sbQuery.append("select * from loan_applied_details lad, loan_details ld where ld.loan_id = lad.loan_id ");
			
			if(uF.parseToInt(getF_type())==1){
				sbQuery.append(" and is_completed = true ");
			}
			if(uF.parseToInt(getF_type())==2){
				sbQuery.append(" and is_completed = false ");
			}
			
			if((getF_service()!=null && getF_service().length>0) || (getF_level()!=null && getF_level().length>0) || (getF_department()!=null && getF_department().length>0) || (getF_strWLocation()!=null && getF_strWLocation().length>0) || (strUserType!=null && !strUserType.equalsIgnoreCase(ADMIN) && (String)session.getAttribute(WLOCATION_ACCESS)!=null) || (uF.parseToInt(getF_org())>0) || (strUserType!=null && !strUserType.equalsIgnoreCase(ADMIN) && (String)session.getAttribute(ORG_ACCESS)!=null)){
				sbQuery.append("and emp_id in (select emp_id from employee_official_details where emp_id > 0 ");
			}
			
			if(uF.parseToInt(getF_org())>0){
				sbQuery.append(" and org_id = "+uF.parseToInt(getF_org()));
			}else if(strUserType!=null && !strUserType.equalsIgnoreCase(ADMIN) && (String)session.getAttribute(ORG_ACCESS)!=null){
					sbQuery.append(" and org_id in ("+(String)session.getAttribute(ORG_ACCESS)+")");
			}
			if(getF_strWLocation()!=null && getF_strWLocation().length>0){
	            sbQuery.append(" and wlocation_id in ("+StringUtils.join(getF_strWLocation(), ",")+") ");
	        }else if(strUserType!=null && !strUserType.equalsIgnoreCase(ADMIN) && (String)session.getAttribute(WLOCATION_ACCESS)!=null){
				sbQuery.append(" and wlocation_id in ("+(String)session.getAttribute(WLOCATION_ACCESS)+")");
			}
            if(getF_department()!=null && getF_department().length>0){
                sbQuery.append(" and depart_id in ("+StringUtils.join(getF_department(), ",")+") ");
            }
            if(getF_level()!=null && getF_level().length>0){
                sbQuery.append(" and grade_id in (select gd.grade_id from grades_details gd,  level_details ld, designation_details dd where gd.designation_id = dd.designation_id and dd.level_id = ld.level_id  and ld.level_id in ( "+StringUtils.join(getF_level(), ",")+") ) ");
            }
            if(getF_service()!=null && getF_service().length>0){
                sbQuery.append(" and (");
                for(int i=0; i<getF_service().length; i++){
                    sbQuery.append(" service_id like '%,"+getF_service()[i]+",%'");
                    
                    if(i<getF_service().length-1){
                        sbQuery.append(" OR "); 
                    }
                }
                sbQuery.append(" ) ");
                
            }
            if((getF_service()!=null && getF_service().length>0) || (getF_level()!=null && getF_level().length>0) || (getF_department()!=null && getF_department().length>0)  || (getF_strWLocation()!=null && getF_strWLocation().length>0) || (strUserType!=null && !strUserType.equalsIgnoreCase(ADMIN) && (String)session.getAttribute(WLOCATION_ACCESS)!=null) || (uF.parseToInt(getF_org())>0) || (strUserType!=null && !strUserType.equalsIgnoreCase(ADMIN) && (String)session.getAttribute(ORG_ACCESS)!=null)){
				sbQuery.append(") ");  
			}
			
			sbQuery.append("order by emp_id ");
			pst = con.prepareStatement(sbQuery.toString());
//			System.out.println("pst=======>"+pst);
			rs = pst.executeQuery();
			while(rs.next()){
				alInner = new ArrayList<String>();
				alInner.add(uF.showData(hmEmpCode.get(rs.getString("emp_id")),""));
				alInner.add(hmEmpName.get(rs.getString("emp_id")));
				alInner.add(uF.showData(hmEmpPanNo.get(rs.getString("emp_id")),""));
				alInner.add(uF.showData(hmOrg.get(hmEmpOrgId.get(rs.getString("emp_id"))),""));
				alInner.add(rs.getString("loan_description"));
				alInner.add(rs.getString("loan_code"));
				alInner.add(rs.getString("amount_paid"));
				alInner.add(uF.showData(rs.getString("loan_interest"), "0")+"%");
				alInner.add(uF.showData(hmAmountPaid.get(rs.getString("emp_id")+"_"+rs.getString("loan_id")), "0"));
				alInner.add(rs.getString("balance_amount"));
				
				alReport.add(alInner);
			}
			rs.close();
			pst.close();
			
			request.setAttribute("alReport",alReport);
			Map<String, String> hmWLocation =CF.getWLocationMap(con,null, null);
			Map<String, String> hmDept =CF.getDeptMap(con); 
			
			request.setAttribute("hmWLocation", hmWLocation);
			request.setAttribute("hmDept", hmDept);
				
		} catch (Exception e) {
			e.printStackTrace(); 
			log.error(e.getClass() + ": " +  e.getMessage(), e);
		}finally{
			db.closeResultSet(rs);
			db.closeStatements(pst);
			db.closeConnection(con);
		}
		return SUCCESS;
	}
	
